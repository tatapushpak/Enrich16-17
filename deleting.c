#include<stdio.h>
void main()
{
	int a[3][3],i,j,k;
	printf("\nEnter the Marix\n");
	for(i=0;i<3;i++)
	{
		for(j=0;j<3;j++)
		{
			scanf("%d",&a[i][j]);
		}
	}
	for(i=0;i<3;i++)
	{
		for(j=0;j<3;j++)
		{
			if(a[i][j]==0)
			{
				a[i][j]='c';
			}
		}
	}
	for(i=0;i<3;i++)
	{
		for(j=0;j<3;j++)
		{
			if(a[i][j]=='c')
			{
				for(k=0;k<3;k++)
				{	
					a[i][k]=0;
					a[k][j]=0;
				}
			}
		}
	}
	for(i=0;i<3;i++)
	{
		printf("\n");
		for(j=0;j<3;j++)
		{
			printf("%d\t",a[i][j]);
		}
	}
}
